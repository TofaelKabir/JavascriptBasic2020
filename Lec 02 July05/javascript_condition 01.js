var age1 = 80;

if (age1 % 2 == 0) {
    console.log("This is Even Number");
}

console.log("\n---------------- if else -----------------")
var age2 = 81;
if (age2 % 2 == 0) {
    console.log("This is Even Number");
} else {
    console.log("This is odd Number");
}

